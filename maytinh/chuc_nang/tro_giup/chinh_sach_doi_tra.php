<b style="color: #ec522b;">2. CÁC TRƯỜNG HỢP YÊU CẦU ĐỔI TRẢ</b>
<p>&ensp;- Lỗi kỹ thuật của sản phẩm - do nhà cung cấp.</p>
<p>&ensp;- Giao nhầm/ giao thiếu.</p>
<p>&ensp;- Chất lượng hàng hóa kém, hư hại do vận chuyển.</p>
<p>&ensp;- Hình thức sản phẩm không giống mô tả ban đầu.</p>
<p>&ensp;- Quý khách đặt nhầm/ không còn nhu cầu.</p>
<b style="color: #ec522b;">3. ĐIỀU KIỆN ĐỔI TRẢ</b>
<p>&ensp;- Sản phẩm còn nguyên bao bì như hiện trạng ban đầu.</p>
<p>&ensp;- Sản phầm còn đầy đủ phụ kiện, quà tặng khuyến mãi kèm theo.</p>
<p>&ensp;- Hóa đơn (bắt buộc trình xuất).</p>
<p>&ensp;- Cung cấp đầy đủ thông tin đối chứng theo yêu cầu.</p>
<b style="color: #ec522b;">4. QUY TRÌNH ĐỔI TRẢ</b>
<p>&ensp;- Video clip quay rõ các mặt của kiện hàng trước khi khui để thể hiện tình trạng của kiện hàng.</p>
<p>&ensp;- Video clip mở kiện hàng từ lúc bắt đầu khui ngoại quan đến kiểm tra sản phẩm bên trong thùng hàng.</p>
<p>&ensp;- Video quay rõ nét , không mờ , nhoè, thể hiện đầy đủ thông tin mã đơn hàng và quay cận cảnh lỗi của sản phẩm.</p>
<p>&ensp;- Hình chụp tem kiện hàng có thể hiện mã đơn hàng.</p>
<p>&ensp;- Hình chụp tình trạng ngoại quan (hình dạng thùng, bao bì,...), đặc biệt các vị trí nghi ngờ có tác động đến sản phẩm (móp méo,...)</p>
<p>&ensp;- Hình chụp tình trạng sản phẩm bên trong, nêu rõ lỗi kỹ thuật nếu có.</p>
<b style="color: #ec522b;">5. CÁCH THỨC CHUYỂN SẢN PHẨM ĐỔI TRẢ VỀ TECHNOLOGY</b>
<p>&ensp;- Khi yêu cầu đổi trả được giải quyết, quý khách vui lòng đóng gói sản phẩm như hiện trạng khi nhận hàng ban đầu.</p>
<p>&ensp;- Hóa đơn (bắt buộc trình xuất).</p>
<p>&ensp;- Quý khách cần quay video clip đóng gói sản phẩm để làm bằng chứng đối liên quan đến đổi trả về sau.</p>
<p>&ensp;- Quý khách vui lòng chịu trách nhiệm về trạng thái nguyên vẹn của sản phẩm khi gửi về TECHNOLOGY.</p>
<p>&ensp;- Sau khi nhận được sản phẩm quý khách gởi về, TECHNOLOGY sẽ phản hồi và cập nhật thông tin trên từng giai đoạn xử lý đến quý khách qua điện thoại/ email</p>
