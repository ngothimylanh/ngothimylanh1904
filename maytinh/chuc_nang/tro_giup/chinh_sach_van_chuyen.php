<b style="color: #ec522b;">2. MỘT SỐ LƯU Ý KHI NHẬN HÀNG</b>
<p>&ensp;- Bưu tá sẽ liên lạc trước khi giao hàng, và nếu không có mặt, sẽ có thêm lần liên lạc.</p>
<p>&ensp;- Nếu không liên lạc được sau 3 lần, đơn hàng có thể bị hủy và tiền sẽ được hoàn trả.</p>
<p>&ensp;- Nếu hàng hóa không đảm bảo chất lượng, không đúng mô tả, hoặc bị hỏng, bạn có quyền từ chối nhận hàng và được hỗ trợ hoàn lại tiền.</p>
<p>&ensp;- Kiểm tra hàng trước khi nhận nếu có dấu hiệu bị hỏng. Bạn có quyền từ chối nhận và yêu cầu đổi hoặc hoàn lại tiền.</p>
<p>&ensp;- Nếu không muốn nhận hàng, liên hệ với bên vận chuyển hoặc CSKH để hủy đơn hàng và nhận lại tiền (trừ chi phí vận chuyển).</p>
<p>&ensp;- TECHNOLOGY sẽ thông báo nếu có chậm trễ giao hàng, và nếu không còn nhu cầu, sẽ hỗ trợ hủy đơn hàng và hoàn lại tiền.</p>
<p>&ensp;- Sản phẩm đóng gói theo tiêu chuẩn, yêu cầu đóng gói đặc biệt có thể phát sinh phí.</p>
<p>&ensp;- Thông báo sớm về thay đổi sản phẩm hoặc hủy đơn hàng để điều chỉnh đơn hàng.</p>
